    public void getData(){
        DefaultTableModel dtm=(DefaultTableModel) this.tbCuaHang.getModel();
        dtm.setRowCount(0);
        for (CuaHang ch : this.chService.getListCH()) {
            Object[] rowdata={
                ch.getId(),ch.getMa(),ch.getTen(),ch.getDiaChi(),ch.getThanhPho(),ch.getQuocGia()
            };
            dtm.addRow(rowdata);
        }
    }
