    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        CuaHang ch=this.getLoadData();
        if (ch==null) {
            return;
        }
        
        this.chService.insert(ch);
        this.getData();
        JOptionPane.showMessageDialog(this, "Thêm thành công");
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        int row=tbCuaHang.getSelectedRow();
        if(row==-1){
            JOptionPane.showMessageDialog(this, "Chọn dòng cần sửa");
        }
        
        CuaHang ch=this.getLoadData();
        if(ch==null){
            return;
        }
        
        this.chService.update(ch, ch.getId());
        this.getData();
        JOptionPane.showMessageDialog(this, "Sửa thành công");
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int row=tbCuaHang.getSelectedRow();
        if(row==-1){
            JOptionPane.showMessageDialog(this, "Chọn dòng cần xóa");
        }else{
            int confirm=JOptionPane.showConfirmDialog(this, "Xác nhận xóa??");
            if(confirm==JOptionPane.YES_NO_OPTION){
                String id=tbCuaHang.getValueAt(row, 0).toString();
                this.chService.delete(id);
                this.getData();
            }
        }
        JOptionPane.showMessageDialog(this, "Xóa thành công");
    }//GEN-LAST:event_btnXoaActionPerformed

    private void tbCuaHangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbCuaHangMouseClicked
        // TODO add your handling code here:
        int row=tbCuaHang.getSelectedRow();
        lblID.setText(tbCuaHang.getValueAt(row, 0).toString());
        txtMa.setText(tbCuaHang.getValueAt(row, 1).toString());
        txtTen.setText(tbCuaHang.getValueAt(row, 2).toString());
        txtDiaChi.setText(tbCuaHang.getValueAt(row, 3).toString());
        cbTP.setSelectedItem(tbCuaHang.getValueAt(row, 4).toString());
        cbQG.setSelectedItem(tbCuaHang.getValueAt(row, 5).toString());
    }//GEN-LAST:event_tbCuaHangMouseClicked

    private CuaHang getLoadData(){
        String id=this.lblID.getText();
        String ma=this.txtMa.getText().trim();
        String ten=this.txtTen.getText().trim();
        String diaChi=this.txtDiaChi.getText().trim();
        String tt=this.cbTP.getSelectedItem().toString();
        String qg=this.cbQG.getSelectedItem().toString();
        
        CuaHang ch=new CuaHang(id, ma, ten, diaChi, tt, qg);
        return ch;
    }
